// scripts/generate_items.mjs
import fs from "node:fs";
const traits = [
  ["מצפוניות", ["מאורגן","מסודר","משימתי","עקבי","שיטתי","מתכנן","עמל"]],
  ["נעימות", ["אמפתי","נעים","מתחשב","שיתופי","מכבד","אכפתי","נגיש"]],
  ["מוחצנות", ["חברותי","דינמי","מוביל","אסרטיבי","מדבר בקלות","אקטיבי","נהנה מקהל"]],
  ["פתיחות מחשבתית", ["סקרן","יצירתי","פתוח לרעיונות","לומד מהר","נמשך לחדש","חוקר","גמיש"]],
  ["יציבות רגשית", ["רגוע","שומר על קור רוח","מאוזן","עמיד ללחץ","אופטימי","בטוח בעצמו","מפוכח"]],
];
const neg = {
  "מצפוניות": ["דוחה משימות","מאבד סדר","מתקשה להתמיד","פועל בלי תכנון","מתפזר בין מטלות"],
  "נעימות": ["חותך אנשים","מתווכח בקלות","שם את עצמי ראשון","ממעט להקשיב","ישיר עד כדי פגיעה"],
  "מוחצנות": ["שומר לעצמי","שקט מאוד בחברה","נמנע מעמידה מול קהל","נרתע מאינטראקציות","מעדיף שלא להוביל"],
  "פתיחות מחשבתית": ["מעדיף שגרה","נמנע מחידושים","מעדיף פתרונות מוכרים","לא נמשך לרעיונות מופשטים","ממעט להתנסות"],
  "יציבות רגשית": ["נסחף מלחץ","נלחץ בקלות","נוטה לדאגות","מתקשה להרגיע את עצמי","נפגע בקלות"],
};
const pos = {
  "מצפוניות": ["מתכנן מראש","עומד ביעדים","שומר על סדר","מקפיד על דדליין","מתמיד גם כשקשה"],
  "נעימות": ["מקשיב לאחרים","ניגש בעדינות","מכבד עמדות שונות","מחפש פתרון משותף","נותן מקום לאחר"],
  "מוחצנות": ["יוזם שיחה","נהנה להוביל","מרגיש נינוח מול קהל","מטעין את הקבוצה באנרגיה","מדבר בביטחון"],
  "פתיחות מחשבתית": ["מחפש זוויות חדשות","נהנה ללמוד תחומים חדשים","מבחין בדפוסים","פתוח לניסוי וטעייה","רואה את התמונה הרחבה"],
  "יציבות רגשית": ["שומר על קור רוח","מוצא פתרון במקום להילחץ","קם מהר מכישלון","מתמודד היטב עם אי-ודאות","אופטימי בסוף"],
};
const stylesPos = ["בדרך כלל","לעיתים קרובות","כשזה חשוב","ביומיום","גם תחת לחץ"];
const stylesNeg = ["לעיתים","כשעמוס","במצבי לחץ","בלי לשים לב","לעתים קרובות"];

function seededRandom(seed){ let t = seed>>>0; return () => { t += 0x6D2B79F5; let r = Math.imul(t ^ (t>>>15), t|1); r ^= r + Math.imul(r ^ (r>>>7), 61); return ((r ^ (r>>>14))>>>0) / 4294967296; }; }
const rand = seededRandom(42);

const records = [];
let id = 1;
const perTrait = 104; // 104*5=520
for (const [trait, syns] of traits) {
  for (let i=0;i<perTrait/2;i++){
    const syn = syns[Math.floor(rand()*syns.length)];
    const phr = pos[trait][Math.floor(rand()*pos[trait].length)];
    const sty = stylesPos[Math.floor(rand()*stylesPos.length)];
    records.push({ id: id++, trait, valence: "חיובי", text: `${sty} אני ${syn} ו${phr}.`});
  }
  for (let i=0;i<perTrait/2;i++){
    const phr = neg[trait][Math.floor(rand()*neg[trait].length)];
    const sty = stylesNeg[Math.floor(rand()*stylesNeg.length)];
    records.push({ id: id++, trait, valence: "שלילי", text: `${sty} אני ${phr}.`});
  }
}
// ערבוב קטן לאקראיות
for (let i=records.length-1;i>0;i--){ const j=Math.floor(rand()*(i+1)); [records[i],records[j]]=[records[j],records[i]]; }

fs.mkdirSync("data", { recursive: true });
fs.writeFileSync("data/shaul_big5_items.json", JSON.stringify(records, null, 2), { encoding: "utf-8" });
console.log("נוצר data/shaul_big5_items.json עם", records.length, "היגדים");
